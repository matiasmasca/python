# Cadenas
# Una cadena es una secuencia de caracteres (letras, números, espacios, marcas de puntuación, etc.) y en Python se distingue porque va encerrada entre comillas simples o dobles.
# Las cadenas pueden usarse para representar información textual: nombres de personas, nombres de los meses, matrı́culas de automovil, un titulo, etc.

print("Curso de Python")
print('Curso de Python')

# salto de linea
print("Curso de \nPython")

# Escapar caracteres
print("Curso de \"Python\"")

# Las cadenas también pueden almacenarse en variables.

frase = "Curso de Python"
print(frase)

# Es posible realizar operaciones con cadenas. Por ejemplo, podemos "sumar" cadenas añadiendo una a otra. Esta operación no se llama suma, sino concatenación y se representa con el signo +.

# Concatenación
print(frase + " esta muy bueno")
print("muy " + "pero " + "muy " + "bueno!")


# Repetición
# podemos repetir una misma cadena un ciertno numero de veces con el signo *
print("Esto es una prueba. " * 3)


